function kansuu([int]$kakaku, [double]$zeiritsu){
    [double]$zeikomi = $kakaku * (1 + $zeiritsu)
    return [Math]::Floor($zeikomi)
}
Write-Host (kansuu 400 0.08)

# -------------------------------------------------
# 解説
# 税込み価格は「価格 ×（1 + 税率）」で計算できます。小数点を切り捨てるために、[Math]::Floor(税込み価格)で計算します。
